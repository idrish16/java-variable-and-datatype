package com.variable;

public class FinnalKeyword {
//	In Java, the final keyword is used to restrict modification.
//	Once something is declared final, it cannot be changed.
	public static void main(String[] args) {
		final int a =10;
//		a=20; // it gives an error because we cannot modify a final variable values. 
		System.out.println(a);
	}
	
	

}
